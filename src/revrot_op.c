#include "pushswap.h"

void	rra(t_list **a, t_list **b)
{
	void(b);
	*a = (*a)->prev;
}

void	rrb(t_list **a, t_list **b)
{
	rra(b, a);
}

void	rrr(t_list **a, t_list **b)
{
	rra(a, b);
	rrb(a, b);
}
