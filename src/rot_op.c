#include "pushswap.h"

void	ra(t_list **a, t_list **b)
{
	void(b);
	*a = (*a)->next;
}

void	rb(t_list **a, t_list **b)
{
	ra(b, a);
}

void	rr(t_list **a, t_list **b)
{
	ra(a, b);
	rb(a, b);
}
